package com.hsp.admin.pojo;

public class Authc {
	
	private String authcId;
	
	private String chain;
	
	private String defenition;
	
	private String createTime;

	public String getAuthcId() {
		return authcId;
	}

	public void setAuthcId(String authcId) {
		this.authcId = authcId;
	}

	public String getChain() {
		return chain;
	}

	public void setChain(String chain) {
		this.chain = chain;
	}

	public String getDefenition() {
		return defenition;
	}

	public void setDefenition(String defenition) {
		this.defenition = defenition;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
	

}
